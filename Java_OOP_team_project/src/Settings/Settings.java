package Settings;

public class Settings {
	
	private int gameTime = 0 ; // In second
	private int maximumRoud = 0 ;
	private int maximumPoint = 0 ;
	public int getGameTime() {
		return gameTime;
	}
	public void setGameTime(int gameTime) {
		this.gameTime = gameTime;
	}
	public int getMaximumRoud() {
		return maximumRoud;
	}
	public void setMaximumRoud(int maximumRoud) {
		this.maximumRoud = maximumRoud;
	}
	public int getMaximumPoint() {
		return maximumPoint;
	}
	public void setMaximumPoint(int maximumPoint) {
		this.maximumPoint = maximumPoint;
	}

	
	
}
